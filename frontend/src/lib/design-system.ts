// Theme utilities
export const getThemeColor = (color: string, shade: number | string = 500) => {
  const colorPath = color.split('.')
  const colorObj = colorPath.reduce((obj: any, key) => obj?.[key], designSystem.colors)
  return colorObj?.[shade] || colorObj
}

export const getSpacing = (size: number | string) => {
  return designSystem.spacing[size as keyof typeof designSystem.spacing] || size
}

export const getBorderRadius = (size: string = 'DEFAULT') => {
  return designSystem.borderRadius[size as keyof typeof designSystem.borderRadius]
}

export const getShadow = (size: string = 'DEFAULT') => {
  return designSystem.shadows[size as keyof typeof designSystem.shadows]
}
